package ir.shirazservice.expert.webservice.workmancredit;

public class WorkmanCredit {
    private String fullName;
    private String credit;
    private String tempCredit;

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getCredit() {
        return credit;
    }

    public void setCredit(String credit) {
        this.credit = credit;
    }

    public String getTempCredit() {
        return tempCredit;
    }

    public void setTempCredit(String tempCredit) {
        this.tempCredit = tempCredit;
    }
}
